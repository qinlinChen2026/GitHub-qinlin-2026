#include <stdio.h>

int main() {
    // 二进制数 101 对应十进制 144
    int a = 0b10111111;
    printf("二进制 10111111 = 十进制 %d\n", a);
    // 二进制数 110 对应十进制 1
    int b = 0b111111000000;
    printf("二进制 111111000000 = 十进制 %d\n", b);  
    // 二进制乘法：101 × 110 = 11110（十进制 30）
    int prod = a * b;
    
    printf("二进制 10111111 × 111111000000 = 十进制 %d\n", prod);
    return 0;
}