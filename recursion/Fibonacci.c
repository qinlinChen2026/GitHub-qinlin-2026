// 故事背景
// 假设规则：
// 刚出生的小兔子，第 1、2 个月不会生，从第 3 个月开始每个月生一对小兔
// 兔子永不死亡
// 一开始只有 1 对刚出生小兔子
// 按月数兔子总数
// 第 1 个月：1 对（小兔，还没长大）
// 第 2 个月：还是 1 对（长大了，但还没生）
// 第 3 个月：原来 1 对 + 生 1 对 👉 2 对
// 第 4 个月：老兔子又生 1 对 👉 3 对
// 第 5 个月：第 3 月的兔子也长大了开始生 👉 5 对
// 第 6 个月：8 对……
// 每月兔子总数：1, 1, 2, 3, 5, 8, 13……
// 你看到有什么规律了么
// 4.27 题目四
// 用递归求斐波那契数列的第 n 项。
// 什么是斐波那契数列
// 规律：
// 第 1 项 = 1
// 第 2 项 = 1
// 从第 3 项开始：当前项 = 前两项之和
// 数列：1, 1, 2, 3, 5, 8, 13, 21, 34...
// 4.27 题目4
// 递归求斐波那契数列第 n 项
#include <stdio.h>

// 递归函数：求第 n 项
int fib(int n)
{
    // 递归终止条件（前两项都是 1）
    if (n == 1 || n == 2)
    {
        return 1;
    }
    // 递归公式：第n项 = 前两项之和
    return fib(n - 1) + fib(n - 2);
}

int main()
{
    int n;
    printf("请输入n：");
    scanf("%d", &n);

    printf("第 %d 项 = %d\n", n, fib(n));
    return 0;
}