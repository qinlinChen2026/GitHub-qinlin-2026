// 题目
// 用双层 for 循环打印出下面的 九九乘法表：
// plaintext
// 1*1=1
// 1*2=2  2*2=4
// 1*3=3  2*3=6  3*3=9
// 1*4=4  2*4=8  3*4=12 4*4=16
// ...
// 1*9=9  2*9=18 ...        9*9=81
// 要求：
// 必须用 两层 for 循环
// 格式整齐对齐
#include <stdio.h>
int main()
{
    int i;
    int j;
    for (i = 1;i <= 9; i++)
    {
        for (j = 1;j <= i; j++)
        {
            printf(" %d*%d =%-2d", j, i, i*j);
        }
        printf("\n");
    }

    return 0;
}